# Create a dictionary with a list as a value
my_dict = {'key1': [1, 2, 3, 4], 'key2': ['a', 'b', 'c']}

# Access the value associated with 'key1'
list_value = my_dict['key1']

# Print the entire list
print(list_value)

# Access specific elements in the list
element_at_index_0 = list_value[0]
element_at_index_2 = list_value[2]

# Print the specific elements
print(element_at_index_0)
print(element_at_index_2)
